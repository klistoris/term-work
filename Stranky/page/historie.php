        <h1 class="nadpis">Historie klubu</h1>
        <hr>
        <p>
            Florbalový klub v Letohradě byl založen v roce 2002 pod názvem Orel Orlice.
            Po dvou úspěšných sezónách jsme oficiální název klubu změnili na FBC Letohrad z důvodu lepšího regionální
            povědomí. V prvních letech za klub nastupovali především hráči v mužských kategoriích, ale v dalších
            letech jsme naši sportovní činnost rozšířili i na ženský tým a starší žáky. Po sedmi letech existence
            se náš klub dostal až do druhé ligy mužů a obsadil v ní třetí příčku. Toto je, společně s vítězstvím v
            poháru nad lídrem první ligy Sokolem Královské Vinohrady, největší klubový úspěch v historii.
            Poté činnost klubu lehce stagnovala, kdy hrál soutěže pouze jeden tým mužů, což zapříčinila zejména
            generační obměna. Tato situace se však změnila v roce 2015, kdy byla nastavena koncepce výchovy mládeže
            a směřování celého klubu. V tuto chvíli máme pět týmů v soutěžích, což je přibližně 90 aktivních hráčů.
            Nadále plánujeme rozšiřovat členskou základnu a budeme se snažit dostat florbal do top sportů v Letohradě.
        </p>

